    private void renterButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renterButton1ActionPerformed
     
        if(this.tripidfield.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Empty Field , Enter Chosen Trip ID", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
           for(int i = 0 ; i < HomePage.Trips.size() ; i++)
           {
             Trip t = (Trip)HomePage.Trips.get(i);
             if(t.getTripID() == Integer.parseInt(this.tripidfield.getText()))
             { 
               t.addTickets(new BusTicket(201, "20 LE"));
               t.addTickets(new BusTicket(204, "22 LE"));
               t.addTickets(new BusTicket(188, "30 LE"));
               t.addTickets(new BusTicket(90, "65 LE"));

                 JOptionPane.showMessageDialog(null, "View Tickets for the Chosen Trip ID");
                 AvailableTickets Page = new AvailableTickets(t);
                 Page.setVisible(true);
                 Page.setLocationRelativeTo(null);
                 dispose();

             }
             
           }
        
        }
      
        
    }//GEN-LAST:event_renterButton1ActionPerformed

