    public AvailableTrips(ArrayList<Trip> Trips) {
        initComponents();
        if(Trips.size() != 0)
        {
          for(int i = 0 ; i < Trips.size() ; i++)
          {
              Trip T = (Trip)Trips.get(i);
              this.IDs += String.valueOf(T.getTripID()) + ',';
              this.RT += T.getBusRunningTime()+" , ";
              this.Fares += T.getFare()+" , ";
              this.TripDates += T.getTripDate()+" , ";

          }
              this.jLabel1.setText(this.IDs);
              this.jLabel2.setText(this.RT);
              this.jLabel3.setText(this.Fares);
              this.jLabel4.setText(this.TripDates);
        }
    }

